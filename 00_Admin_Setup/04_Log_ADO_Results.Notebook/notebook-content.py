def get_auth_header():
    """Creates Basic Auth header for ADO using PAT."""
    encoded_pat = base64.b64encode(f":{ADO_PAT}".encode()).decode()
    return {"Authorization": f"Basic {encoded_pat}"}

def upload_attachment(file_path):
    """
    Uploads a file to ADO and returns the Attachment URL.
    """
    if not file_path or not os.path.exists(file_path):
        print(f"File not found for upload: {file_path}")
        return None

    filename = os.path.basename(file_path)
    url = f"{ADO_ORG}/{ADO_PROJECT}/_apis/wit/attachments?fileName={filename}&api-version=7.1"
    
    headers = get_auth_header()
    headers["Content-Type"] = "application/octet-stream"
    
    print(f"   ⬆️ Uploading {filename}...")
    try:
        with open(file_path, "rb") as f:
            data = f.read()
        
        response = requests.post(url, headers=headers, data=data)
        response.raise_for_status()
        
        # Return the URL used to link this attachment to a work item
        return response.json()["url"]
    except Exception as e:
        print(f"Upload failed: {e}")
        return None

def create_child_bug(parent_id, title, comment_html, attachments_urls=[]):
    """
    Creates a Bug (or Issue), links it to the Parent, adds description, and links attachments.
    """
    # Change to $Bug if your process uses Bugs, otherwise $Issue
    url = f"{ADO_ORG}/{ADO_PROJECT}/_apis/wit/workitems/$Issue?api-version=7.1"
    headers = get_auth_header()
    headers["Content-Type"] = "application/json-patch+json"
    
    # 1. Basic Fields
    patch_ops = [
        {"op": "add", "path": "/fields/System.Title", "value": title},
        {"op": "add", "path": "/fields/System.History", "value": comment_html} # Discussion Field
    ]
    
    # 2. Link to Parent (Hierarchy-Reverse)
    if parent_id:
        parent_url = f"{ADO_ORG}/{ADO_PROJECT}/_apis/wit/workItems/{parent_id}"
        patch_ops.append({
            "op": "add", 
            "path": "/relations/-", 
            "value": {
                "rel": "System.LinkTypes.Hierarchy-Reverse",
                "url": parent_url,
                "attributes": {"comment": "Auto-generated by Fabric Pipeline"}
            }
        })
    
    # 3. Link Attachments
    for att_url in attachments_urls:
        patch_ops.append({
            "op": "add",
            "path": "/relations/-",
            "value": {
                "rel": "AttachedFile",
                "url": att_url,
                "attributes": {"comment": "Error Log"}
            }
        })
        
    print(f"Creating Work Item '{title}'...")
    response = requests.post(url, headers=headers, json=patch_ops)
    response.raise_for_status()
    
    new_id = response.json()["id"]
    print(f"Work Item Created: ID {new_id}")
    return new_id

